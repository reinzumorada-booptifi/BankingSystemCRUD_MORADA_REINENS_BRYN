    private void refreshTransactionTable() {
    DefaultTableModel model = (DefaultTableModel) tblRecentTrans.getModel();
    model.setRowCount(0); 

    try {
        BankDAO dao = new BankDAO();
        ResultSet rs = dao.getAllTransactions();
        
        while (rs != null && rs.next()) {
            Vector<Object> row = new Vector<>();
            
            // Column 0: Transaction ID (The 1, 2, 3, 4, 5 on the far left)
            row.add(rs.getInt("transaction_id"));
            
            // Column 1: Account ID (e.g., 1001, 1002)
            row.add(rs.getString("account_id"));
            
            // Column 2: Transaction Type (Withdraw / Deposit)
            row.add(rs.getString("transaction_type"));
            
            // Column 3: Amount (e.g., 200.0)
            row.add(rs.getDouble("amount"));
            
            // Column 4: Date
            row.add(rs.getTimestamp("transaction_date"));
            
            model.addRow(row);
        }
    } catch (SQLException e) {
        e.printStackTrace();
    }
}
    
    
    
