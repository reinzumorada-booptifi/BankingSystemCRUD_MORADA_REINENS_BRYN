    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        String id = txtAccountID.getText();

    if (id.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Select an account or enter an ID first.");
        return;
    }

    // Confirmation Pop-up
    int confirm = JOptionPane.showConfirmDialog(this, 
            "Are you sure you want to delete Account " + id + "?", 
            "Confirm Deletion", JOptionPane.YES_NO_OPTION);

    if (confirm == JOptionPane.YES_OPTION) {
        BankSystems logic = new BankSystems();
        if (logic.deleteAccount(id)) {
            JOptionPane.showMessageDialog(this, "Account deleted successfully.");
            
            // Clear fields and refresh table
            txtAccountID.setText("");
            txtFirstName.setText("");
            txtLastName.setText("");
            logic.populateTable(tblAccounts); 
        } else {
            JOptionPane.showMessageDialog(this, "Delete failed. Account ID not found.");
        }
    }
    }//GEN-LAST:event_btnDeleteActionPerformed

