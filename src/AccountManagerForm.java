    private void btnApplyFilterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApplyFilterActionPerformed
        // 1. Get the Account ID from your text field
    String searchID = txtFilterID.getText().trim(); 

    // 2. Clear the table completely before the new search
    // This ensures only "one" search result (or one account's history) shows at a time
    DefaultTableModel model = (DefaultTableModel) tblFullHistory.getModel();
    model.setRowCount(0);

    // 3. Prevent empty searches from crashing
    if (searchID.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please enter an Account ID to search.");
        return;
    }

    try {
        BankDAO dao = new BankDAO();
        // Use the Filtered method we set up in the DAO
        // We pass "Account ID" as the type so the SQL knows to use: WHERE account_id = ?
        ResultSet rs = dao.getFilteredTransactions("Account ID", searchID);
        
        boolean found = false;
        while (rs != null && rs.next()) {
            found = true;
            Vector<Object> row = new Vector<>();
            row.add(rs.getString("account_id"));
            row.add(rs.getString("transaction_type"));
            row.add(rs.getDouble("amount"));
            row.add(rs.getTimestamp("transaction_date"));
            model.addRow(row);
        }
        
        if (!found) {
            javax.swing.JOptionPane.showMessageDialog(this, "No transactions found for Account ID: " + searchID);
        }

    } catch (SQLException e) {
        e.printStackTrace();
    }
    }//GEN-LAST:event_btnApplyFilterActionPerformed

